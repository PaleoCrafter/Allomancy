package de.mineformers.investiture.client.util;

/**
 * ${JDOC}
 */
public class Colour
{
    private final float r, g, b, a;

    public Colour(float r, float g, float b, float a)
    {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }

    public float r()
    {
        return r;
    }

    public float g()
    {
        return g;
    }

    public float b()
    {
        return b;
    }

    public float a()
    {
        return a;
    }
}
